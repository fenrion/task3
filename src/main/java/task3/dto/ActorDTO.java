package task3.dto;

public class ActorDTO {
    private int id;
    private String name;

    public ActorDTO() {
    }

    public ActorDTO(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
